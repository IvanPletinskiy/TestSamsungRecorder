    private static synchronized Pattern getPattern() {
        Pattern pattern;
        synchronized (EmoticonUtils.class) {
            if (sPattern == null) {
                StringBuilder sb = new StringBuilder(getUnicodeList().size() * 2);
                sb.append('(');
                Iterator<String> it = getUnicodeList().iterator();
                while (it.hasNext()) {
                    sb.append(Pattern.quote(unicodeToUTF16(it.next())));
                    sb.append('|');
                }
                sb.replace(sb.length() - 1, sb.length(), ")");
                sPattern = Pattern.compile(sb.toString());
            }
            pattern = sPattern;
        }
        return pattern;
    }

    protected static String unicodeToUTF16(String str) {
        int intValue = Integer.decode(str).intValue();
        if (str.length() - 2 > 4) {
            int i = ((((2031616 & intValue) >> 16) - 1) << 6) | ((intValue & 64512) >> 10) | 55296;
            int intValue2 = Integer.decode("0x" + Integer.toHexString(56320 | (intValue & 1023))).intValue();
            int intValue3 = Integer.decode("0x" + Integer.toHexString(i)).intValue();
            return "" + ((char) intValue3) + ((char) intValue2);
        }
        return "" + ((char) intValue);
    }

